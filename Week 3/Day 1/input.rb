# Ruby Data Types


# 'gets' function accepts user input from terminal
puts "What is your name?"
name = gets
puts "You typed that your name is: "
puts name

# The String is a piece of text stored in quotations
text = "hello"

# The integer can be of type Fixnum or Bignum and stores whole numbers, positive and negative
number = 3

#A boolean value is a either true or false
b = true

# An array can store many ruby types in a list
arr = [text, b, number, "bye", :there]

# A hash stores key and value pairs
hash = {:name => "Jon", :number => 18003445342, :job => :instructor}

